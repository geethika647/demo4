//JAVA PROGRAM TO ILLUSTRATE UNARY BITWISE COMPLEMENT OPERATOR
class Bitwisecomplement
{
public static void main(String args[])
{
int n1=6,n2=-2;
//printng numbers on console
System.out.println("First number="+n1);
System.out.println("Second number="+n2);
//printing numbers on console after performing bitwise complement
System.out.println(n1+"'s bitwise complement="+~n1);
System.out.println(n2+"'s bitwise complement="+~n2);
}
}

