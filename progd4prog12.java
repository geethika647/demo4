class bitwiseleftrightshift
{
public static void main(String args[])
{
int number=2;
int result=number<<1;//1 bit left shif operator
System.out.println(result);//2*2 prints 4
int n=3;
int r=n<<2;//2 bit left shift operation
System.out.println(r);//3*2*2 prints 12
int x=20;
int y=x>>1;
System.out.println(y);//20/2 prints 10
int p=80;
int q=p>>3;
System.out.println(q);// 80/2/2/2 prints 10
}
}